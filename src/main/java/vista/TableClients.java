    private void loadClientsTable() {
        mdClientsTable.setRowCount(0); //de esta forma vaciamos la tabla
        List<String[]> clients = controladorClient.selectClients();
        clients.forEach(client -> {
            mdClientsTable.addRow(client);
        });
    }

